% Παράμετροι της κανονικής κατανομής
mu = 4;        % Μέσος όρος
sigma = 0.1;   % Τυπική απόκλιση (sqrt(0.01))

% Α. Πιθανότητα να απορριφθεί μια σιδηροδοκός (X < 3.9)
p_reject = normcdf(3.9, mu, sigma);
fprintf('Πιθανότητα απόρριψης: %.4f\n', p_reject);

% Β. Όριο για να καταστρέφεται το πολύ το 1% των σιδηροδοκών
threshold_1percent = norminv(0.01, mu, sigma);
fprintf('Όριο για καταστροφή στο 1%%: %.4f\n', threshold_1percent);
